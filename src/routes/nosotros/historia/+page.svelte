<h1 class="text-3xl text-center p-4 m-4 font-bold text-gray-600">Historia</h1>

<main class="max-w-3xl mx-auto px-10 lg:px-0 text-justify">
    <p class="my-4">El Instituto de Educación Superior Tecnológico Público «MOTUPE», del Distrito de MOTUPE, fue creado
        en el segundo gobierno del Arquitecto Fernando Belaúnde Terry, siendo Ministro de Educación el Dr. Valentín
        Demetrio Paniagua Corazao el 14 de Setiembre del año 1984 mediante Resolución Ministerial Nº 1135-84-ED, siendo
        Alcalde el señor Sebastián Vilela Purihuaman quien tuvo una decidida gestión en compañía de una comisión de
        Motupanos en su creación, con el apoyo del Senador de la República Dr. Eduardo Yashimura Montenegro.</p>
    <p class="my-4">Posteriormente en el año de 1986 entra en funcionamiento con la Carrera Profesional de Agropecuaria
        con 2 secciones «A» y «B» (45 alumnos cada sección) en el local en la calle Poeta Emiliano Niño Nº 155 (local
        donde funcionó el Colegio Cruz de Chalpón) donde destacó la persona del alumno Anselmo Saavedra Sánchez quien
        propone el logotipo de la institución modificado en la actualidad, teniendo como su primer Director al Ingeniero
        Agrónomo Anhuor Paz Larraín (Período: 1986- 1996) sus docentes fundadores Lic. Alonso Vilela Chunga, Ing.
        Sebastián Lino Sandoval Gutiérrez.</p>
    <p class="my-4">En el año de 1987 empieza a funcionar la Carrera Profesional de Administración teniendo como docente
        fundador al Econ. Miguel Andrés Eneque Pisfil, posteriormente en el año 1996 mediante Resolución Directoral Nº
        255 de 24 de Mayo de 1996 comienzan a funcionar las Carreras Profesionales de Enfermería Técnica y Computación e
        Informática. Por el año de 1992 el señor Alcalde Carlos Cortez Serquén dona 20,000 m2 de terreno del fundo
        «Pasco» del municipio a favor del Instituto y con el apoyo del Gobierno Local CORDELAM se construye en dicho
        terreno el nuevo local con un pabellón de 4 aulas para posteriormente ir creciendo en infraestructura. Nuestro
        segundo Director el Ing. Jorge Valverde Castro (Período: 1997-1999) y posteriormente el Ing. Héctor Zenón Reupo
        Durand (Período: 1999-2015), Lic. Alonso Vilela CHunga (Periodo 2016), Ing. Ana Rosa Santamaría Muro (Periodo
        2017), Ing. Sebastián Lino Sandoval Gutiérrez (Periodo 2018) y en la actualidad el Ing. José Elías Vasquez
        Carranza (Periodo 2019-2020) , contando con una plana docente de 13 Nombrados y 10 docentes contratados, 05
        personal administrativo y de servicio y con un total de 377 alumnos.</p>
